# Variable
# Variable can be start with A-Z or a-z or _
# can not be started with 0-9
# it should not contain special characters &, # @ etc
# Varibles are case-sensitive

age = 30
name1 = "Hemant"
Name2 = "Barot"
isMale = True
pi = 3.14

print(age)
print(name1)
print(Name2)
print(isMale)
print(pi)

print(type(age))
print(type(name1))
print(type(Name2))
print(type(isMale))
print(type(pi))