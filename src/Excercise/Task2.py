num1 = int(input("Enter Number 1 : "))
num2 = int(input("Enter Number 2 : "))

print("Maximum number out of both is", max(num1, num2))
print("Power of num1 ^ num2 = ", pow(num1, num2))
print("Sum of num1 & num2 = ", f"{num1} + {num2}")
print("subtraction of num1 & num2 = ", f"{num1} - {num2}")
print("Multiplication of num1 & num2 = ", f"{num1} * {num2}")
print("Division of num1 & num2 = ", f"{num1} / {num2}")
